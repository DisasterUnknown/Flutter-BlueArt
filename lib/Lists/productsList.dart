List<Item> CartList = [];

// Favorits List
List<Item> FavoritList = [];

// Items Class
class Item {
  String id;
  String title;
  String price;
  String discount;
  String discription;
  String imageURL;
  int quality;

  Item({
    required this.id,
    required this.title,
    required this.price,
    required this.discount,
    required this.discription,
    required this.imageURL,
    required this.quality,
  });

  // Adding items to the FavoritList
  static void addFavorite(Item item) {
    FavoritList.add(item);
  }

  // Removing items from the FavoritList
  static void removeFavorit(Item item) {
    FavoritList.remove(item);
  }

  // Adding items to the cartList
  static void addProduct(Item item, int quality) {
    item.quality = quality;
    CartList.add(item);
  }

  // Removing items to the cartList
  static void removeProduct(Item item) {
    CartList.remove(item);
  }

  // Updating items to the cartList
  static void updateProduct(Item item, int quality) {
    for (int i = 0; i < CartList.length; i++) {
      if (CartList[i].id == item.id) {
        CartList[i].quality = quality;
        break;
      }
    }
  }
}
